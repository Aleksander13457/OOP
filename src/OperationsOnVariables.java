public class OperationsOnVariables {
    public static void main(String[] args) {
        int number = 21;
        if (number % 7 == 0) {
            System.out.println("The number 7 is the divisor of the number " + number);
        } else {
            System.out.println("The number 7 is not a divisor of the number " + number);
        }
    }
}